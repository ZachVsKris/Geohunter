alter table public.stat_categories
  add column if not exists review_status text not null default 'candidate',
  add column if not exists evidence_tier text,
  add column if not exists auto_qualified boolean not null default false,
  add column if not exists common_year smallint,
  add column if not exists common_year_coverage integer not null default 0,
  add column if not exists official_observation_share double precision,
  add column if not exists modeled_observation_share double precision,
  add column if not exists clustering_score smallint,
  add column if not exists stability_score smallint,
  add column if not exists methodology_notes text,
  add column if not exists quality_standard_version text not null default 'strict-v1';

-- Add constraints separately so this migration remains idempotent.
do $$
begin
  if not exists (
    select 1 from pg_constraint
    where conname = 'stat_categories_review_status_check'
      and conrelid = 'public.stat_categories'::regclass
  ) then
    alter table public.stat_categories
      add constraint stat_categories_review_status_check
      check (review_status in ('candidate','needs_review','approved','rejected'));
  end if;

  if not exists (
    select 1 from pg_constraint
    where conname = 'stat_categories_evidence_tier_check'
      and conrelid = 'public.stat_categories'::regclass
  ) then
    alter table public.stat_categories
      add constraint stat_categories_evidence_tier_check
      check (evidence_tier is null or evidence_tier in ('A','B','C'));
  end if;

  if not exists (
    select 1 from pg_constraint
    where conname = 'stat_categories_share_range_check'
      and conrelid = 'public.stat_categories'::regclass
  ) then
    alter table public.stat_categories
      add constraint stat_categories_share_range_check
      check (
        (official_observation_share is null or official_observation_share between 0 and 1)
        and (modeled_observation_share is null or modeled_observation_share between 0 and 1)
      );
  end if;

  if not exists (
    select 1 from pg_constraint
    where conname = 'stat_categories_quality_subscore_check'
      and conrelid = 'public.stat_categories'::regclass
  ) then
    alter table public.stat_categories
      add constraint stat_categories_quality_subscore_check
      check (
        (clustering_score is null or clustering_score between 0 and 100)
        and (stability_score is null or stability_score between 0 and 100)
      );
  end if;
end $$;

create index if not exists stat_categories_review_status_idx
  on public.stat_categories (review_status, source_organization);

create index if not exists stat_categories_auto_qualified_idx
  on public.stat_categories (auto_qualified, review_status)
  where auto_qualified = true;

create or replace function public.clear_stat_source_observations(
  p_source_organization text,
  p_source_dataset text
)
returns bigint
language plpgsql
security definer
set search_path = public
as $$
declare
  removed bigint;
begin
  delete from public.stat_observations
  where category_id in (
    select id from public.stat_categories
    where source_organization = p_source_organization
      and source_dataset = p_source_dataset
  );
  get diagnostics removed = row_count;
  return removed;
end;
$$;

revoke all on function public.clear_stat_source_observations(text,text) from public, anon, authenticated;
grant execute on function public.clear_stat_source_observations(text,text) to service_role;

create table if not exists public.stat_category_reviews (
