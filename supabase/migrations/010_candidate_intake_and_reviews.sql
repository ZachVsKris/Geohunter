  category_id text not null references public.stat_categories(id) on delete cascade,
  reviewer_user_id uuid not null,
  decision text not null check (decision in ('approved','rejected','reset')),
  notes text,
  quality_snapshot jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default now()
);

create index if not exists stat_category_reviews_category_idx
  on public.stat_category_reviews (category_id, created_at desc);

alter table public.stat_category_reviews enable row level security;

-- Existing World Bank rows keep their current playable state. Future external
-- imports default to quarantine until explicitly reviewed.
update public.stat_categories
set
  review_status = case when eligible_daily then 'approved' else 'candidate' end,
  auto_qualified = eligible_daily,
  evidence_tier = case when eligible_daily then 'B' else evidence_tier end,
  common_year = coalesce(common_year, latest_available_year),
  common_year_coverage = greatest(common_year_coverage, country_coverage),
  quality_standard_version = 'legacy-world-bank-v1'
where source_organization = 'World Bank'
  and review_status = 'candidate';

